package member.Entity;

/**
 * @author admin
 * @version 1.0.0
 */
public class Member {
    private  String name;

    private  int age;

    private  int port;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
